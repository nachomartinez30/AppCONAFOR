package gob.conafor.pc.mod;

public class CEAccesibilidadPC {

    private int AccesibilidadID;
    private int upmID;
    private int medioTransporteID;
    private int viaAccesoID;
    private float distancia;
    private int condicionAccesoID;

    public int getAccesibilidadID() {
        return AccesibilidadID;
    }

    public void setAccesibilidadID(int AccesibilidadID) {
        this.AccesibilidadID = AccesibilidadID;
    }

    public int getUpmID() {
        return upmID;
    }

    public void setUpmID(int upmID) {
        this.upmID = upmID;
    }

    public int getMedioTransporteID() {
        return medioTransporteID;
    }

    public void setMedioTransporteID(int medioTransporteID) {
        this.medioTransporteID = medioTransporteID;
    }

    public int getViaAccesoID() {
        return viaAccesoID;
    }

    public void setViaAccesoID(int viaAccesoID) {
        this.viaAccesoID = viaAccesoID;
    }

    public float getDistancia() {
        return distancia;
    }

    public void setDistancia(float distancia) {
        this.distancia = distancia;
    }

    public int getCondicionAccesoID() {
        return condicionAccesoID;
    }

    public void setCondicionAccesoID(int condicionAccesoID) {
        this.condicionAccesoID = condicionAccesoID;
    }

}
