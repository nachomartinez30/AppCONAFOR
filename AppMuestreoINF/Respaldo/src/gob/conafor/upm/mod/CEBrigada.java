package gob.conafor.upm.mod;

public class CEBrigada {

    private int upmID;
    private int brigadistaID;
    private int puestoID;
    private int empresaID;

    public int getUpmID() {
        return upmID;
    }

    public void setUpmID(int upmID) {
        this.upmID = upmID;
    }

    public int getBrigadistaID() {
        return brigadistaID;
    }

    public void setBrigadistaID(int brigadistaID) {
        this.brigadistaID = brigadistaID;
    }

    public int getPuestoID() {
        return puestoID;
    }

    public void setPuestoID(int puestoID) {
        this.puestoID = puestoID;
    }

    public int getEmpresaID() {
        return empresaID;
    }

    public void setEmpresaID(int empresaID) {
        this.empresaID = empresaID;
    }
}
