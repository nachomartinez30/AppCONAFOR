package gob.conafor.sys.mod;

public class CESeccionesCapturadas {

    private int upmId;
    private int seccionId;
    private int estaus;
    private int sitio;

    public int getUpmId() {
        return upmId;
    }

    public void setUpmId(int upmId) {
        this.upmId = upmId;
    }
    
    public int getSeccionId() {
        return seccionId;
    }

    public void setSeccionId(int seccionId) {
        this.seccionId = seccionId;
    }

    public int getEstaus() {
        return estaus;
    }

    public void setEstaus(int estaus) {
        this.estaus = estaus;
    }

    public int getSitio() {
        return sitio;
    }

    public void setSitio(int sitio) {
        this.sitio = sitio;
    }
    
}
