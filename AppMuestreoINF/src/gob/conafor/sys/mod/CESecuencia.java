package gob.conafor.sys.mod;

public class CESecuencia {
    private int upmId;
    private int sitio;
    private int formatoID;
    private int capturado;

    public int getUpmId() {
        return upmId;
    }

    public void setUpmId(int upmId) {
        this.upmId = upmId;
    }

    public int getSitio() {
        return sitio;
    }

    public void setSitio(int sitio) {
        this.sitio = sitio;
    }

    public int getFormatoID() {
        return formatoID;
    }

    public void setFormatoID(int formatoID) {
        this.formatoID = formatoID;
    }

    public int getCapturado() {
        return capturado;
    }

    public void setCapturado(int capturado) {
        this.capturado = capturado;
    }
}
