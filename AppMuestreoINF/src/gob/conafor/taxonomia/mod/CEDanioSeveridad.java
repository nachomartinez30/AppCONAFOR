package gob.conafor.taxonomia.mod;

public class CEDanioSeveridad {

    Integer seccionID;
    Integer numeroDanio;
    Integer agenteDanioID;
    Integer severidadID;

    public Integer getSeccionID() {
        return seccionID;
    }

    public void setSeccionID(Integer seccionID) {
        this.seccionID = seccionID;
    }

    public Integer getNumeroDanio() {
        return numeroDanio;
    }

    public void setNumeroDanio(Integer numeroDanio) {
        this.numeroDanio = numeroDanio;
    }

    public Integer getAgenteDanioID() {
        return agenteDanioID;
    }

    public void setAgenteDanioID(Integer agenteDanioID) {
        this.agenteDanioID = agenteDanioID;
    }

    public Integer getSeveridadID() {
        return severidadID;
    }

    public void setSeveridadID(Integer severidadID) {
        this.severidadID = severidadID;
    }

}
