package gob.conafor.taxonomia.mod;

public class CESubmuestraObservaciones {

    private Integer SubmuestraObservacionesID;
    private Integer SitioID;
    private String Observaciones;

    public Integer getSubmuestraObservacionesID() {
        return SubmuestraObservacionesID;
    }

    public void setSubmuestraObservacionesID(Integer SubmuestraObservacionesID) {
        this.SubmuestraObservacionesID = SubmuestraObservacionesID;
    }

    public Integer getSitioID() {
        return SitioID;
    }

    public void setSitioID(Integer SitioID) {
        this.SitioID = SitioID;
    }

    public String getObservaciones() {
        return Observaciones;
    }

    public void setObservaciones(String Observaciones) {
        this.Observaciones = Observaciones;
    }

}
